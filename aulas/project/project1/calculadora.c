#include <stdio.h>
#include "calculadora.h"

float adic(float num1, float num2){

    return num1 + num2;

}

float sub(float num1, float num2){

    return num1 - num2;

}

float mult(float num1, float num2){

    return num1 * num2;

}

float div(float num1, float num2){

    return num1 / num2;

}